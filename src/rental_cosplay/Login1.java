    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        // TODO add your handling code here:
        String username = text_username.getText().trim();
        String password = text_password.getText().trim();

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Username dan Password tidak boleh kosong.", "Login Gagal", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Menggunakan parameterized query untuk mencegah SQL injection
            String query = "SELECT * FROM user WHERE username = ? AND password = ?";
            this.stat = k.getCon().prepareStatement(query);
            this.stat.setString(1, username);
            this.stat.setString(2, password);
            this.rs = this.stat.executeQuery();

            if (rs.next()) {
                String level = rs.getString("level");
                String namaUser = rs.getString("nama_user");

                // Menentukan tindakan berdasarkan level
                if ("admin".equalsIgnoreCase(level)) {
                    JOptionPane.showMessageDialog(this, "Login berhasil. Selamat datang, " + namaUser, "Login Sukses", JOptionPane.INFORMATION_MESSAGE);
                    Menu_admin reg = new Menu_admin();
                    reg.setVisible(true);
                    this.setVisible(false);
                } else if ("user".equalsIgnoreCase(level)) {
                    JOptionPane.showMessageDialog(this, "Login berhasil. Selamat datang, " + namaUser, "Login Sukses", JOptionPane.INFORMATION_MESSAGE);
                    Menu_pelanggan kostum = new Menu_pelanggan();
                    kostum.setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Role tidak dikenali: " + level, "Login Gagal", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Username atau Password salah.", "Login Gagal", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_loginActionPerformed

